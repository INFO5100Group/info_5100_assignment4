    private void populateDetails(){
        SimpleDateFormat sdf =   new SimpleDateFormat( " yyyy-MM-dd " ); 
        String time = sdf.format(flight.getDepartTime());
        jtxDate.setText(time);
        jtxName.setText(flight.getFlightName());
        ComboAr.removeAllItems();
        ComboDe.removeAllItems();
        ComboTime.removeAllItems();
        ComboTime.addItem(flight.getDaytime());
        ComboDe.addItem(flight.getDeparture());
        ComboAr.addItem(flight.getDestination());
    }

