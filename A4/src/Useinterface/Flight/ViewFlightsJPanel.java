    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        flight.setFlightName(jtxName.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dates = jtxDate.getText();
        Date date;
        try {
            date = sdf.parse(dates);
            flight.setDepartTime(date);
        } catch (ParseException ex) {
        }
        String[] adress = { "Boston", "New York", "San Francisco","Washington D.C", "Seattle", "Austin" };
        String[] time = { "Morning", "Daytime", "Evening"};
        ComboAr = new JComboBox(adress);
        ComboDe = new JComboBox(adress);
        ComboTime = new JComboBox(time);
        flight.setDeparture(ComboDe.getSelectedItem().toString());
        flight.setDestination(ComboAr.getSelectedItem().toString());
        flight.setDaytime(ComboTime.getSelectedItem().toString());
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Flight updated successfuly");
    }//GEN-LAST:event_btnSaveActionPerformed

